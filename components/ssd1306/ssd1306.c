struct ssd1306 {
    uint8_t framebuf[OLED_WIDTH * OLED_PAGES];
    i2c_master_bus_handle_t bus;
    i2c_master_dev_handle_t dev;
};

/* ── Low-level I2C write ─────────────────────────────────────────── */

static esp_err_t oled_write_cmd(ssd1306_t *dev, uint8_t cmd)
{
    uint8_t buf[2] = { 0x00, cmd };  /* Co=0, D/C#=0 → command */
    return i2c_master_transmit(dev->dev, buf, sizeof(buf), I2C_TIMEOUT_MS);
}

static esp_err_t oled_write_data(ssd1306_t *dev, const uint8_t *data, size_t len)
{
    uint8_t *buf = malloc(1 + len);
    if (!buf) return ESP_ERR_NO_MEM;

    buf[0] = 0x40;  /* Co=0, D/C#=1 → data */
    memcpy(&buf[1], data, len);
    esp_err_t ret = i2c_master_transmit(dev->dev, buf, 1 + len, I2C_TIMEOUT_MS);
    free(buf);
    return ret;
}

/* ── Public API ──────────────────────────────────────────────────── */

esp_err_t ssd1306_init(ssd1306_t **out_dev)
{
    esp_err_t ret;

    ssd1306_t *dev = calloc(1, sizeof(ssd1306_t));
    if (!dev) return ESP_ERR_NO_MEM;

    /* ── 1. Vext ON: GPIO36 LOW enables 3.3V to OLED ────────────── */
    ESP_LOGI(TAG, "Enabling Vext (GPIO%d LOW)...", OLED_VEXT_CTRL);
    gpio_reset_pin(OLED_VEXT_CTRL);
    gpio_set_direction(OLED_VEXT_CTRL, GPIO_MODE_OUTPUT);
    gpio_set_level(OLED_VEXT_CTRL, 0);
    vTaskDelay(pdMS_TO_TICKS(100));

    /* ── 2. Hardware reset ───────────────────────────────────────── */
    ESP_LOGI(TAG, "OLED reset (GPIO%d)...", OLED_RST);
    gpio_reset_pin(OLED_RST);
    gpio_set_direction(OLED_RST, GPIO_MODE_OUTPUT);
    gpio_set_level(OLED_RST, 1);
    vTaskDelay(pdMS_TO_TICKS(10));
    gpio_set_level(OLED_RST, 0);
    vTaskDelay(pdMS_TO_TICKS(50));
    gpio_set_level(OLED_RST, 1);
    vTaskDelay(pdMS_TO_TICKS(50));

    /* ── 3. I2C bus init (new master driver) ────────────────────── */
    i2c_master_bus_config_t bus_cfg = {
        .i2c_port   = I2C_PORT,
        .sda_io_num = OLED_SDA,
        .scl_io_num = OLED_SCL,
        .clk_source = I2C_CLK_SRC_DEFAULT,
        .flags.enable_internal_pullup = true,
    };

    ret = i2c_new_master_bus(&bus_cfg, &dev->bus);
    if (ret != ESP_OK) {
        ESP_LOGE(TAG, "i2c_new_master_bus failed: %s", esp_err_to_name(ret));
        goto err;
    }

    /* ── 4. Probe OLED on I2C bus ────────────────────────────────── */
    ESP_LOGI(TAG, "Probing I2C for OLED at 0x%02X...", OLED_I2C_ADDR);
    ret = i2c_master_probe(dev->bus, OLED_I2C_ADDR, I2C_TIMEOUT_MS);
    if (ret != ESP_OK) {
        ESP_LOGE(TAG, "OLED NOT FOUND at 0x%02X! Scanning bus...", OLED_I2C_ADDR);
        for (uint8_t addr = 0x01; addr < 0x7F; addr++) {
            if (i2c_master_probe(dev->bus, addr, 50) == ESP_OK) {
                ESP_LOGW(TAG, "  Found device at 0x%02X", addr);
            }
        }
        ret = ESP_ERR_NOT_FOUND;
        goto err;
    }
    ESP_LOGI(TAG, "OLED found at 0x%02X", OLED_I2C_ADDR);

    /* ── 4b. Add OLED as I2C device ──────────────────────────────── */
    i2c_device_config_t dev_cfg = {
        .dev_addr_length = I2C_ADDR_BIT_LEN_7,
        .device_address  = OLED_I2C_ADDR,
        .scl_speed_hz    = I2C_FREQ_HZ,
    };

    ret = i2c_master_bus_add_device(dev->bus, &dev_cfg, &dev->dev);
    if (ret != ESP_OK) {
        ESP_LOGE(TAG, "i2c_master_bus_add_device failed: %s", esp_err_to_name(ret));
        goto err;
    }

    /* ── 5. SSD1306 init sequence ────────────────────────────────── */
    static const uint8_t init_cmds[] = {
        0xAE,       /* Display OFF */
        0xD5, 0x80, /* Set clock div */
        0xA8, 0x3F, /* Set mux ratio: 63 (64 lines) */
        0xD3, 0x00, /* Display offset: 0 */
        0x40,       /* Start line: 0 */
        0x8D, 0x14, /* Charge pump: enable */
        0x20, 0x00, /* Memory addressing: horizontal */
        0xA1,       /* Segment remap: col 127 → SEG0 */
        0xC8,       /* COM scan: remapped */
        0xDA, 0x12, /* COM pins: alternative, no L/R remap */
        0x81, 0xCF, /* Contrast: 207 */
        0xD9, 0xF1, /* Pre-charge: phase1=1, phase2=15 */
        0xDB, 0x40, /* VCOMH deselect: ~0.77×Vcc */
        0xA4,       /* Entire display ON (from RAM) */
        0xA6,       /* Normal (not inverted) */
    };

    for (size_t i = 0; i < sizeof(init_cmds); i++) {
        ret = oled_write_cmd(dev, init_cmds[i]);
        if (ret != ESP_OK) {
            ESP_LOGE(TAG, "Init cmd[%d]=0x%02X failed: %s",
                     (int)i, init_cmds[i], esp_err_to_name(ret));
            goto err;
        }
    }

    /* Clear display RAM */
    ssd1306_clear(dev);
    ret = ssd1306_update(dev);
    if (ret != ESP_OK) {
        ESP_LOGE(TAG, "Initial clear failed: %s", esp_err_to_name(ret));
        goto err;
    }

    /* Display ON */
    ret = oled_write_cmd(dev, 0xAF);
    if (ret != ESP_OK) {
        ESP_LOGE(TAG, "Display ON failed: %s", esp_err_to_name(ret));
        goto err;
    }

    ESP_LOGI(TAG, "SSD1306 OLED ready (128x64)");
    *out_dev = dev;
    return ESP_OK;

err:
    free(dev);
    return ret;
}

void ssd1306_clear(ssd1306_t *dev)
{
    memset(dev->framebuf, 0x00, sizeof(dev->framebuf));
}

void ssd1306_draw_string(ssd1306_t *dev, uint8_t row, uint8_t col, const char *str)
{
    if (row >= OLED_PAGES) return;

    uint8_t *page = &dev->framebuf[row * OLED_WIDTH];

    for (; *str && col < (OLED_WIDTH - 5); str++) {
        uint8_t c = (uint8_t)*str;
        if (c < 32 || c > 126) c = '?';

        const uint8_t *glyph = font6x8[c - 32];
        for (int i = 0; i < 6 && col < OLED_WIDTH; i++, col++) {
            page[col] = glyph[i];
        }
    }
}

esp_err_t ssd1306_update(ssd1306_t *dev)
{
    esp_err_t ret;

    /* Set column and page addresses for full screen */
    ret = oled_write_cmd(dev, 0x21);  if (ret != ESP_OK) return ret;  /* Col addr */
    ret = oled_write_cmd(dev, 0x00);  if (ret != ESP_OK) return ret;  /* Start: 0 */
    ret = oled_write_cmd(dev, 0x7F);  if (ret != ESP_OK) return ret;  /* End: 127 */
    ret = oled_write_cmd(dev, 0x22);  if (ret != ESP_OK) return ret;  /* Page addr */
    ret = oled_write_cmd(dev, 0x00);  if (ret != ESP_OK) return ret;  /* Start: 0 */
    ret = oled_write_cmd(dev, 0x07);  if (ret != ESP_OK) return ret;  /* End: 7 */

    /* Send framebuffer page by page (128 bytes each) */
    for (int p = 0; p < OLED_PAGES; p++) {
        ret = oled_write_data(dev, &dev->framebuf[p * OLED_WIDTH], OLED_WIDTH);
        if (ret != ESP_OK) {
            ESP_LOGE(TAG, "Update page %d failed: %s", p, esp_err_to_name(ret));
            return ret;
        }
    }

    return ESP_OK;
}
